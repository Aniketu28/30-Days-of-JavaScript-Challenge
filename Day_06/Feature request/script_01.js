// use of push pop unshif and shift method

const Arr = [1,2,3,4,5];

Arr.push(6);
console.log("added element in last index :",Arr);

Arr.pop();
console.log("removed element from last index :",Arr);

Arr.unshift(0);
console.log("added element in first index :",Arr);

Arr.shift();
console.log("removed element from first index :",Arr);

